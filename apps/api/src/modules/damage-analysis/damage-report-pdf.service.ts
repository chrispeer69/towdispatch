/**
 * DamageReportPdfService — renders damage-analysis reports with PDFKit
 * (Photo Damage Analysis, Session 42).
 *
 * Mirrors InvoicePdfService: imperative PDFKit, renders to a Buffer (the
 * controller streams it; no disk, no storage coupling), bilingual EN/ES.
 *
 * Photo overlays: rather than embedding the source S3 image bytes (which
 * would couple rendering to object storage and make the output non-
 * deterministic / untestable), each bounding box is drawn as a labelled
 * rectangle on a normalised photo frame — the operator sees WHERE on the
 * photo the model flagged damage. Embedding the real photo behind the
 * overlay is a deferred enhancement (see SESSION_42_DECISIONS.md).
 */
import { Injectable } from '@nestjs/common';
import type {
  CompareResult,
  DamageAnalysisDto,
  DamageComparisonDto,
  DamageFindingDto,
  DamageSeverity,
} from '@ustowdispatch/shared';
import PDFDocument from 'pdfkit';

export type PdfLanguage = 'en' | 'es';

export interface DamageReportContext {
  jobReference: string;
  vehicleDescription: string;
  operatorName: string;
  language?: PdfLanguage;
}

export interface RenderAnalysisReportInput {
  analysis: DamageAnalysisDto;
  findings: DamageFindingDto[];
  context: DamageReportContext;
}

export interface RenderComparisonReportInput {
  comparison: DamageComparisonDto;
  result: CompareResult;
  context: DamageReportContext;
}

type DamageLabels = {
  analysisTitle: string;
  comparisonTitle: string;
  job: string;
  vehicle: string;
  phase: string;
  generated: string;
  operator: string;
  analyzedBy: string;
  status: string;
  findings: string;
  area: string;
  severity: string;
  confidence: string;
  description: string;
  noFindings: string;
  overlays: string;
  newDamage: string;
  preExisting: string;
  inconclusive: string;
  summary: string;
  operatorOverride: string;
  dismissed: string;
  footer: string;
};

